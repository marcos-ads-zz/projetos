
public class Funcionario {
	String nome;
	double salaio;
	public Funcionario(String nome, Cargo cargo, double salario) {
		this.nome = nome;
		this.salaio = salario;	
	}
	public Cargo cargo() {
		return cargo();
	}
	public String getNome() {
		return nome;
	}
	public double getSalaio() {
		return salaio;
	}
	

}
